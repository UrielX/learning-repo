fruit_or_veggie = input()

if fruit_or_veggie == "banana" or fruit_or_veggie == "apple" or fruit_or_veggie == "kiwi" or fruit_or_veggie == "cherry" or fruit_or_veggie == "lemon" or fruit_or_veggie == "grapes":
    print("fruit")
elif fruit_or_veggie == "tomato" or fruit_or_veggie == "cucumber" or fruit_or_veggie == "pepper" or fruit_or_veggie == "carrot":
    print("vegetable")
else:
    print("unknown")